public class variabledec {

    public static void main(String[] args) {
        
        int num = 10;
        float dec = 10.5f;
        char letter = 'c';
        boolean tf = true;
        short num1 = 500;
        long num2 = 50_600_000;
        double dec1 = 56.69;

        System.out.println("The value of num is :"+num+" and the value of float is: "+dec);
        System.out.println("The value of long is "+num2+" and the value of double is: "+dec1);
        System.out.println("The value of boolean is: "+tf);
        System.out.println("The value of short is: "+num1+" and the value of char is: "+letter);
    }
}