public class typecasting {

    public static void main(String[] args){
        
        int a = 510;
        byte b = 5;

        float f = 5.6f;
        int c = (int)f;

        byte b1 = 45;
        byte b2 = 20;
        b = (byte)a;

        int result = b1 * b2;

        System.out.println("The value of a is: "+ b);
        System.out.println("The value of c is: "+ c);
        System.out.println("The value of result is: "+ result);
    
    }


    
}
